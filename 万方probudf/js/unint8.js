function uint8ToPlain(u8, type = 'utf8') {
  switch (type) {
    case 'utf8': return new TextDecoder().decode(u8);
    case 'hex':  return Array.from(u8, b => b.toString(16).padStart(2, '0')).join('');
    case 'base64': return btoa(String.fromCharCode(...u8));
    case 'ascii': return String.fromCharCode(...u8);
    default: throw new Error('unsupported type');
  }
}

