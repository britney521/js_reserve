class CustomAdapter(requests.adapters.HTTPAdapter):
    def init_poolmanager(self, *args, **kwargs):
        context = create_urllib3_context()
        context.minimum_version = ssl.TLSVersion.TLSv1_2
        list_ciphers="49196-49195-49200-49199-159-158-49188-49187-49192-49191-49162-49161-49172-49171-157-156-61-60-53-47-10".split("-")
        str_list=[]
        for item in list_ciphers:
            str_ciphers=dict_map[int(item)]
            if str_ciphers not in [
                "TLS_AES_256_GCM_SHA384"
                "TLS_CHACHA20_POLY1305_SHA256"
                "TLS_AES_128_GCM_SHA256"
            ]:
                str_list.append(str_ciphers)

        context.set_ciphers(":".join(str_list))
        for item in context.get_ciphers():
            print(item["name"],item["protocol"])
        kwargs["ssl_context"] = context
        return super().init_poolmanager(*args, **kwargs)

